    signal dbm_val_reg   : std_logic_vector(31 downto 0) := (others => '0');
    signal dbm_ready_reg : std_logic := '0';
begin

    -- Register çıkış bağlantıları
    dbm_val   <= dbm_val_reg;
    dbm_ready <= dbm_ready_reg;

    process(clk, reset)
    begin
        if reset = '1' then
            dbm_val_reg   <= (others => '0');
            dbm_ready_reg <= '0';
        elsif rising_edge(clk) then
            
            if xadc_ready = '1' then
                -- 1. Adım: Veri geldiği an LUT'tan kararlı değeri çekip register'a yazıyoruz
                dbm_val_reg   <= DBM_LUT(to_integer(unsigned(xadc_data)));
                -- 2. Adım: Verinin hazır olduğunu bildiriyoruz
                dbm_ready_reg <= '1';
            else
                -- El Sıkışma Mantığı: Eğer xadc_ready düştüyse ve main_fsm yeni adıma/taramaya geçtiyse
                -- dbm_ready sinyalini ancak o zaman güvenle sıfırlayabiliriz.
                -- Bu sayede main_fsm sinyali asla kaçıramaz.
                if dbm_ready_reg = '1' then
                    dbm_ready_reg <= '0';
                end if;
            end if;
            
        end if;
    end process;
    
end Behavioral;
